              <h4>Polish and maintain aluminum rims</h4>
              <br>
              Shiny aluminum rims once again give the impression of a new and well-maintained car. However, compared to the robust steel rims, they also require particularly gentle care. To remove heavy dirt, you should only use a special rim cleaner that does not attack the aluminum surface. Smaller cracks and hard-to-reach areas can be cleaned with a toothbrush. Once the dirt has been completely removed, you can polish the rims with a cloth and then apply a sealant.
              <br>
              <br>
              <h2>
                Removing paint scratches – efficient and quick help
              </h2>
              <br>
              <br>
              After all, scratches in car paint are not only annoying, but can also have a negative impact during sales discussions. Depending on how deep the paint scratch is, you have several options for treating it. So first test with your finger whether you feel any resistance in the affected area and whether it even shows a different color than the actual paint color.
              <ul>
                <li>
                    If this is not the case, it is just minor damage to the paint, which you can make disappear by cleaning and applying a polish. Rub the mass over the affected area in circular movements and then let it dry thoroughly. Afterwards, you can use a polishing cloth to wipe it carefully and in quick movements.
                </li>
                <li>
                    For deeper scratches, you can work with a touch-up pen in the appropriate color. To do this, you first need the exact color code of your car, which you can research on the manufacturer's website. When applying, you should take your time and keep a steady hand so that you only treat the visible scratch and work as cleanly as possible.
                </li>
              </ul>
