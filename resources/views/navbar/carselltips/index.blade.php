            <h1>Selling a car – tips for preparation and care</h1>
            <p>magine : Your used car is technically still in pretty good shape, but visually it no longer looks that great. A few scratches in the paint, dirty rims, a windshield wiper that no longer does what it's supposed to and the last time the interior was cleaned was a long time ago - so the car makes a worse impression than it should. However, this can be changed quickly with thorough cleaning or professional preparation. With a clean appearance and a freshly cleaned interior you can really score points with potential buyers. Not only does your old used car get a wellness treatment, a good care program can also have a positive effect on the sales price.

               <br> We'll show you what you can expect from professional cleaning and also how you can do it yourself and achieve a good result. This way, both you and your used car can really shine when selling your car.</p>
             <h2>Used car check and residual value determination</h2>
             <p>
                Before you invest a lot of money in a thorough preparation, you should clarify what residual value you can expect for the old car. After all, it's not worth investing a lot of money in advance in cleaning if the sales price is hardly higher. This is an important first step in preparing to sell a used car . Many online used car portals offer a free value assessment . You can enter your vehicle's data into the search mask provided to find out within a few minutes what the average sales value of your car model is. Once you're on a site like this, it's also worth taking a look at the sales section. Look at how similar vehicles are presented and the price set by the seller. Alternatively, you can have your car assessed by a professional car appraiser. Here you not only get information about the residual value, but also about the technical and functional condition the car is currently in.
             </p>
             <h2>Selling a car: Cleaning brings plus points</h2>
             <p>If the car is used almost every day, it is obvious that dirt will accumulate on the inside and outside of the vehicle. Pollen dust in summer and road salt deposits in winter do not leave a car unnoticed. Even after a trip through the car wash, residue can remain on the vehicle, which in the worst case can lead to long-term damage to the materials. However, you can prevent it and help maintain the value of your car by having it professionally cleaned every year or by thoroughly washing it by hand on a regular basis. And that ultimately pays off when you sell your car. A well-maintained used car that looks similar to a new car will achieve a higher selling price than an unkempt car. We will therefore show you how you can prepare your car to make a good impression on both private buyers and dealers.</p>
             <h4>Have your car washed by a professional</h4>
             <p>
                Professional cleaning or preparation of a vehicle is worthwhile in many ways: On the one hand, the used car is thoroughly checked and scratches, stains and minor damage are treated and repaired directly. On the other hand, you save yourself an enormous amount of work, time and money, as you ultimately need the right materials and the necessary knowledge for thorough cleaning. Polishing a car in particular can make a big impression when it comes to selling it, but it's not a particularly worthwhile task to do by hand. We have put together a list of services that can be carried out by professionals in addition to polishing.
             </p>
             <h4>Professional car preparation: what does it involve?</h4>
             <br>
             <br>
             <ul >
                <li >Exterior cleaning</li>
                <li >Paint polishing and sealing</li>
                <li >Mirror and window cleaning</li>
                <li >Repair of scratches and stone chips</li>
                <li >Engine wash</li>
                <li >Underbody washing</li>
                <li >Roof sealing on convertibles</li>
                <li >Changing wheels and cleaning rims</li>
                <li >Cleaning the interior</li>
                <li >Stain removal on upholstery and leather seats</li>
                <li >Carpet cleaning</li>
              </ul>
              <p>Anyone who decides to have it professionally prepared in a workshop or car cleaning center must of course weigh up the costs. Depending on the condition and care behavior of the owner, these are individual. Depending on the size of the car, you can usually get a simple interior and exterior cleaning for just under €100 - €150. However, this does not remove paint damage or stubborn stains. If you are interested in a thorough cleaning, you should expect a price range of €200 - €300, although there is no upper limit.

              </p>
             <h4>Wash your car yourself – this is how you do it</h4>
             <p>If the costs for professional cleaning are simply too high or the investment is no longer worthwhile considering the used car, good results can also be achieved by hand. It's best to work from the inside out and have the necessary materials ready . A visit to the local gas station is ideal for this, as you can usually find both a high-pressure cleaner and a vacuum cleaner here.</p>
             <h5>Car cleaning in 10 steps</h5>
             <ol >
                <li >Cleaning up and emptying the car</li>
                Before you can start properly cleaning, the car must be freely accessible. This means that the interior and trunk must be completely emptied and any accumulated waste removed.

                <li >Interior vacuum</li>
                These are best cleaned with a microfibre cloth in combination with warm water and a special car cleaner. Change the cleaning water several times to work thoroughly, and wipe with a dry towel to avoid water stains. Take enough time to remove the dust that has accumulated in cracks and openings that are difficult to reach.
                dirt from the soles of shoes, food residue, etc. can be removed with a thorough vacuum cleaner pass through the entire interior. The narrow nozzle head is ideal for narrow spaces and cracks and also gets crumbs, hair, etc. out of and between the seats.
                <li >Cleaning fittings</li>
                You should also thoroughly knock out the floor mats and clean them with a vacuum cleaner. When it comes to the seats, it all depends on the material and the degree of dirt. You should remove conspicuous stains as best as possible by thoroughly cleaning the upholstery or using appropriate leather care products so that they do not stand out negatively when sold.
                <li >Cleaning floor mats and upholstery</li>
                Constant use can cause dust and dirt to settle on the rubber seals and impair protection against wind and water. Therefore, you should wipe them completely wet and let them dry thoroughly. Then go over it again with a special rubber care product. This keeps the door rubbers supple and maintains their elasticity. The plastic strips are also thoroughly cleaned. If these have faded over time due to sun, wind and weather, you can get it back under control with a little plastic care.
                <li >Cleaning the interior</li>
                This is where the high-pressure cleaner comes in. This should be kept at a safe distance of around 30cm to avoid causing dents. Afterwards you have to invest a lot of time in hand washing to thoroughly cover every corner. Equipped with car shampoo, a large bucket of water, a sponge and a towel, it is best to work your way from front to back.
                <li >Stain removal on upholstery and leather seats</li>
                Various deposits on the windows can usually be removed with a commercially available window cleaner. For stubborn dirt, it's best to use a special cleaner. The windows must be wiped from both the outside and the inside and make sure to also clean the upper part of the windows, which is only visible when the windows are lowered. In addition, you should not forget to align the side and interior mirrors correctly after you have cleaned them.
                <li >Carpet cleaning</li>
                With the right care of the car paint, you can specifically prevent some damage and restore the old shine. To do this, you need a clean microfiber cloth, polishing liquid, adhesive tape to mask plastic strips, and above all one thing: a lot of patience. It's best to work your way forward in small steps, area by area. Apply some polish with the cloth and rub the car paint with light pressure until no residue is visible. A subsequent treatment with a little car wax seals the polish for a longer period of time.
              </ol>
              <br>
              <h4>Don’t forget to clean the rims!</h4>
              <br>
              The rims also contribute to a well-maintained overall impression and should therefore not be neglected. In order to remove the coarse dirt, you first work with a high-pressure cleaner and then remove stubborn residues with a sponge, all-purpose cleaner and warm water. Brake dust and tar deposits in particular often stick to the rims and are difficult to remove without chemical cleaners. Here you should always follow the instructions and then rinse the rims with clear water.

