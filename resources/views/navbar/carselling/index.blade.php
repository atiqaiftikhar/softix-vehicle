     <h4 >How do I sell my car?</h4>
     <p>Regardless of whether you want to sell your car privately or to a dealer, there are a few things to consider in each case. Using the following list, you can easily proceed step by step so as not to forget any important point.</p>
     <h4>Get information and set a price</h4>
     <p>If you are well prepared, you can also appear confident in sales discussions. The most important factor is the price of the used car. If this is set too high, potential buyers will be put off and you will not receive any inquiries. If the price is too low, the phone will probably ring continuously and you won't make any real profit in the end. To find out the exact residual value of your car, you can have a report issued by TÜV, DEKRA or Schwacke. However, since this involves certain costs, you can alternatively simply do your own research and get an idea of ​​the current market situation. On mobile.de you have the opportunity to enter all the data about your used car such as make, model, equipment, registration, etc. into a search mask and then receive a real sales value. It's also worth taking a look at other sellers' advertisements so that you can get a certain feel for how much you can charge for your car. Most sellers initially set the amount higher in order to retain a certain amount of leeway at their own desired price.

     </p>
     <h4>2. Car care and preparation</h4>
     <p>First of all, a used car should have no major defects and be in a roadworthy condition. The trip to the general inspection is therefore inevitable and should ideally be planned shortly before the sale. It can be worthwhile to have minor scratches, dents and paint damage repaired, as a well-kept appearance is positive and usually leaves a good overall impression on the buyer. Thorough cleaning of the car (outside and inside) also pays off. A used car is of course not a new car, but the more you can give the impression of it, the better the chances of selling it and you can achieve a higher selling price. If you want to create an online advertisement, you should also take care of attractive images . On the one hand, the car should be shown from its best side, but any defects must also be clearly recorded.</p>
     <h4>3.a Create an advertisement for private sales</h4>
     <p>If the car is in top condition both visually and technically and has been captured in pictures, then you can continue with the advertisement on used car portals such as mobile.de. If you want to be successful when selling used cars, you should be particularly careful to describe the condition in which the car is. This means that all important details should be stated truthfully and special equipment such as electronic parking assistance, cruise control, Apple Carplay, etc. should be explicitly highlighted. Existing defects should be communicated honestly and documented with appropriate pictures. This not only ensures transparency, but also creates a certain level of trust before the meeting so that interested parties know exactly where they stand. In addition, there will be no unpleasant surprises or disappointments afterwards. Finally, an option for the price must be selected. The decision between a negotiated basis and a fixed price rests solely with you, but room for negotiation leaves a feeling of say on the part of the buyer.</p>
     <h4>3.b Find a suitable dealer</h4>
     <p>If you want to sell your car to a dealer or a purchasing point, the vehicle will be inspected by them at an agreed appointment. So you don't have to worry about suitable pictures or advertisement descriptions, but the thorough cleaning will benefit you here too. A well-maintained car also leaves a better impression on a dealer and can have a positive effect on the residual value estimate. On mobile.de you can easily search for a suitable dealer in your area and make an appointment directly.</p>
     <h4>Necessary documents</h4>
     <p>So that nothing can go wrong on the day of the sale, all documents should be collected in advance and ready to hand. This includes, on the one hand, the vehicle keys, registration certificate parts I and II (vehicle license and vehicle registration document) as well as the inspection certificate and the service booklet. Documents regarding maintenance and repair work, warranty certificates, and any acceptance and registration papers regarding changes to the car should also be brought with you to the handover. When making a sale with a private buyer, a verified sample contract form for both parties should not be missing.</p>
     <h4>5. Test drive and purchase contract</h4>
     <p>Before a prospective buyer decides to buy, the important test drive takes place. The potential new owner gets a precise feeling of how the car drives and feels. There are several things to consider here. First, you should check with your insurance company whether another driver is allowed to drive your car. If the car has already been deregistered, you will also have to take care of a short-term license plate or a so-called red number. Basically, you should have the interested party's ID and driver's license shown to you before you start your journey. And the golden rule for all test drives is: always ride in the car yourself. If everything has gone well and both parties can agree on a price, then the purchase can be sealed by signing a contract. Both the buyer and the seller should provide all data carefully.</p>
     <h4>Inform the relevant authorities</h4>
     <p>After the sale, the responsible registration office and the vehicle insurance company must be informed about the purchase or sale of the vehicle. It also makes sense to have the buyer record the re-registration contractually so that you don't have to continue paying taxes on the car that has already been sold. If you sell your car to a dealer, they will also take care of deregistering the vehicle.</p>
