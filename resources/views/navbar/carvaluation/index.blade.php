<h3 class="text-center">Different value types</h3>
<div class="row">
    <div class="col-8">
        <p>
            When determining the value of a car, it must be taken into account that there are different types of value. Our car valuation works with the help of constantly updated market value monitoring with more than 1.4 million vehicles. This is how we determine realistic vehicle values ​​and calculate a supra-regional market value for all vehicles.<br>
            When it comes to car valuation, there are other options and terms that need to be taken into account. The current value, for example, describes the value of the used item at a specified point in time. The replacement value is the price you would have to pay if you were to buy the same car again. The replacement value may differ from the market value.
            <br>
            The purchase price indicates the price that a dealer would pay. The price is also often called the purchase price. It is usually lower than the selling price because the dealer ultimately wants to make money from the sale. The list value is the price that the car costs according to the list. This can be the new price list or the used car list from Schwacke or other providers.
