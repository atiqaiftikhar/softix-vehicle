 <h2 class="text-center">Other assessment methods</h2>
 <div class="row">
    <div class="col-4">
        <img src="https://images.prismic.io/shacarlacca/a9eb7f40-892f-4155-a215-b30932d80a44_bewertung-kapitel-7.jpg?ixlib=gatsbyFP&auto=compress%2Cformat&fit=max">

    </div>
    <div class="col-8">
        <p>
            mobile.de's sophisticated valuation matrix calculates the current market value of a used car. Based on around 1.4 million vehicles, we calculate a daily, free car valuation in seconds and give you a fair price for your car.

            But there are other methods to create a used car valuation. The oldest basis is a list from Schwacke. Since 1957, it has been providing the approximate residual value of used vehicles based on vehicle type, year of manufacture, equipment and mileage. The DAT list also regularly determines the values ​​of used vehicles as a basis for dealer purchases.<br>
           Individual car dealerships also offer a dealer appraisal to determine the value of the car. The basis for the evaluation is often lists from Schwacke or DAT.
        </p>

    </div>
    <p>
        Car clubs such as the ADAC also prepare a used car check report for a fee, and the work is carried out in one of the nationwide testing centers. Official testing institutes such as TÜV, Dekra or GTÜ also prepare used car reports for a fee. There are also a number of freelance experts who also prepare reports or valuation reports for a fee. Depending on the scope, this includes a fresh general inspection, a technical and visual check and a paint layer thickness measurement.


